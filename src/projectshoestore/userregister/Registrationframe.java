    private void registerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerBtnActionPerformed
        try {
            String name,uName,password,catagory;
            catagory = userCatcombobox.getSelectedItem().toString();
            String sql = "SELECT UserId FROM hamroshoestore.usercat where UserCatagory='"+catagory+"';";
            PreparedStatement psd = DbUtil.getConnection().prepareStatement(sql);
            ResultSet rs = psd.executeQuery();
            int id = 0;
            while(rs.next()){
                id = rs.getInt("UserId");
            }
            name =nameTxtbox.getText();
            uName=usernameTxtbox.getText();
            password= passwordTxtbox.getText();
           
            String sq = "INSERT INTO `hamroshoestore`.`userinfo` ( `name`, `username`, `password`, `catagory`) VALUES ('"+name+"', '"+uName+"', '"+password+"', '"+id+"');";
            Statement ps = DbUtil.getConnection().prepareStatement(sq);
            ps.executeUpdate(sq);
            
        } catch (SQLException ex) {
            Logger.getLogger(Registrationframe.class.getName()).log(Level.SEVERE, null, ex);
        }
        nameTxtbox.setText("");
        usernameTxtbox.setText("");
        passwordTxtbox.setText("");
        this.dispose();
        //JOptionPane.showMessageDialog(null,"register sucessfully");
        
        
        
    }//GEN-LAST:event_registerBtnActionPerformed

