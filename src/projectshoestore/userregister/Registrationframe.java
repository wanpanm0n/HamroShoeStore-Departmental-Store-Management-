    private void itemsCombobox() {
         try {
             String sql = "SELECT * FROM hamroshoestore.usercat;";
             String catName;
            PreparedStatement psd = DbUtil.getConnection().prepareStatement(sql);
            ResultSet rs = psd.executeQuery();
            while(rs.next()){
                catName = rs.getString("UserCatagory");
                userCatcombobox.addItem(catName);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Registrationframe.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }
